def ler_temp():
  c = float(input('Insira o valor da temperatura em graus Celsius: '))
  return c

def c_para_f(c):
  f = (9.0 * c + 160.0)/5.0
  return f

def resultado(f):
  print('A temperatura em Fahrenheit é:', f)

c = ler_temp()
f = c_para_f(c)
resultado(f)

