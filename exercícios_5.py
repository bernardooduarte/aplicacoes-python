def ler_dados():
  tempo_gasto = float(input('Insira o tempo gasto na viagem: '))
  velocidade_media = float(input('Insira a velocidade média durante a viagem: '))
  return tempo_gasto, velocidade_media

def distancia(tempo_gasto, velocidade_media):
  d = tempo_gasto * velocidade_media
  return d

def qtd_litros(d):
  litros_usados = d / 12
  return litros_usados

def imprimir_resultados(tempo_gasto, velocidade_media, d, litros_usados):
  print('Tempo gasto:', tempo_gasto)
  print('Velocidade média:', velocidade_media)
  print('Distância:', d)
  print('Litros usados:', litros_usados)

tempo, velocidade = ler_dados()
d = distancia(tempo, velocidade)
litros = qtd_litros(d)
imprimir_resultados(tempo, velocidade, d, litros)